package jiajiaofabu;

public class User {
	 private String afbujigou;//发布机构
	 private String nianji;//年级
     private String kemu;//科目
     private String tele;//联系方式
     private String money;//薪资
     private String student;//学生情况
     private String yaoqiu;//应聘要求
	 public User(){}

	    public String getJigou() {
	        return afbujigou;
	    }

	    public void setJigou(String afbujigou) {
	        this.afbujigou = afbujigou;
	    }

	    public String getNianji() {
	        return nianji;
	    }

	    public void setNianji(String nianji) {
	        this.nianji = nianji;
	    }
	    public String getKemu() {
	        return kemu;
	    }

	    public void setKemu(String kemu) {
	        this.kemu = kemu;
	    }
	    
	    public String getTele() {
	        return tele;
	    }

	    public void setTele(String tele) {
	        this.tele = tele;
	    }
	    
	    public String getMoney() {
	        return money;
	    }

	    public void setMoney(String money) {
	        this.money = money;
	    }
	    public String getStudent() {
	        return student;
	    }

	    public void setStudent(String student) {
	        this.student = student;
	    }
	    public String getYaoqiu() {
	        return yaoqiu;
	    }

	    public void setYaoqiu(String yaoqiu) {
	        this.yaoqiu = yaoqiu;
	    }
}
